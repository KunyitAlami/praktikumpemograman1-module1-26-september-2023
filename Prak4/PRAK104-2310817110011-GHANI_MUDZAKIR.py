sepatu_a = 400000
sepatu_b = 350000
harga_diskon_sepatu_a = sepatu_a*13/100
harga_diskon_sepatu_b = sepatu_b*21/100

print("Harga Sepatu A adalah ", sepatu_a);
print("Harga Sepatu B adalah ", sepatu_b);
print("Sepatu A mendapat diskon 13% sehingga harganya menjadi {:.0f}".format(sepatu_a-harga_diskon_sepatu_a));
print("Sepatu B mendapat diskon 21% sehingga harganya menjadi {:.0f}".format(sepatu_b-harga_diskon_sepatu_b));